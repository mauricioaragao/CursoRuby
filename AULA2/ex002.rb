# Saída de dados
print 'Digite o primeiro número inteiro: '
firstNamber = gets.chomp.to_f

print 'Digite o segundo número inteiro: '
secondNumber = gets.chomp.to_f

addition = firstNamber + secondNumber
puts "A adição é: #{addition}"

subtracion = firstNamber - secondNumber
puts "A subtração é: #{subtracion}"

division = firstNamber / secondNumber
puts "A divisão é: #{division}"

multiplication = firstNamber * secondNumber
puts "A multiplicação é: #{multiplication}"